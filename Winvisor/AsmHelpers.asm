PUBLIC InveptOp
PUBLIC GetGDTBase
PUBLIC GetGDTLimit
PUBLIC GetIDTBase
PUBLIC GetIDTLimit
PUBLIC GetTR
PUBLIC GetCS
PUBLIC GetDS
PUBLIC GetSS
PUBLIC GetES
PUBLIC GetFS
PUBLIC GetGS
