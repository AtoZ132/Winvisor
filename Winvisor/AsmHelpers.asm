PUBLIC GetLDTR

_text SEGMENT

; Invept Assembly procedure helper
InveptOp PROC PUBLIC

INVEPT RCX, OWORD PTR [RDX]
RET

InveptOp ENDP

; The 16-bit limit field of the register is stored in the low 2 bytes 
; And the 64-bit base address is stored in the high 8 bytes.
GetGDTBase PROC PUBLIC

LOCAL GDTR[10]:BYTE
SGDT GDTR
MOV RAX, QWORD PTR GDTR[2]
RET

GetGDTBase ENDP

GetGDTLimit PROC PUBLIC

LOCAL GDTR[10]:BYTE
SGDT GDTR
MOV AX, WORD PTR GDTR[0]
RET

GetGDTLimit ENDP

; The 16-bit limit field of the register is stored in the low 2 bytes 
; And the 64-bit base address is stored in the high 8 bytes.
GetIDTBase PROC PUBLIC

LOCAL IDTR[10]:BYTE
SIDT IDTR
MOV RAX, QWORD PTR IDTR[2]
RET

GetIDTBase ENDP

GetIDTLimit PROC PUBLIC

LOCAL IDTR[10]:BYTE
SIDT IDTR
MOV AX, WORD PTR IDTR[0]
RET

GetIDTLimit ENDP

GetTR PROC PUBLIC

STR RAX
RET

GetTR ENDP

GetCS PROC PUBLIC

MOV RAX, CS
RET

GetCS ENDP

GetDS PROC PUBLIC

MOV RAX, DS
RET

GetDS ENDP

GetSS PROC PUBLIC

MOV RAX, SS
RET

GetSS ENDP

GetES PROC PUBLIC

MOV RAX, ES
RET

GetES ENDP

GetFS PROC PUBLIC

MOV RAX, FS
RET

GetFS ENDP

GetGS PROC PUBLIC

MOV RAX, GS
RET

GetGS ENDP

GetRflags PROC PUBLIC

PUSHFQ
POP RAX
RET

GetRflags ENDP

GetLDTR PROC PUBLIC

SLDT RAX
RET

GetLDTR ENDP

_text ENDS
END